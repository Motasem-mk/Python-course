# [ == ] Equal
# [ != ] Not equal
# [ > ] Greater than 
# [ < ] Less than 
# [ >= ] Greater than or Equal
# [ <= ] Less than or   Equal


print(100==100)
print(100==200)
print("Motasem" == "Motasem")
print(100 == 100.00)
print("^" * 30)

print(100!=100)
print(100!=200)
print("Motasem" != "Motasem")
print(100 != 100.00)

print("^" * 30)
 
#  Greater than +  Less than 
print(100>100)
print(100<200)
print("Motasem" > "Motase") # one letter less
print(100 < 100.00)

# [ >= ] Greater than or Equal
# [ <= ] Less than or   Equal
print("^" * 30)
print(100 >= 100)
print(100 >= 200)
print(100 >= 100.00)
print(100 >= 40)

print("^" * 30)
print(100 <= 100)
print(100 <= 200)
print(100 <= 100.00)
print(100 <= 40)
